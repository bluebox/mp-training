package Main;

public class Shift {
	public static void main(String args[]) {
		int shiftNumber = 8;
		
		System.out.println("shiftNumber << 2 is "+(shiftNumber << 2));
		System.out.println("shiftNumber >> 3 is "+(shiftNumber >> 3));
		System.out.println("shiftNumber >>> 3 is "+(shiftNumber >>> 3));
	}
}
