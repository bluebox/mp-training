
public class ArithmeticOperatorts {
    public static void main(String[] args) {
        int a = 3;
        int b = 6;

        int sum = a+b;
        int sub = a-b;
        int mul = a * b;
        int div = (int) a/b;
        int mod = a%b;


        System.out.println("sum : " + sum);
        System.out.println("sub : " + sub);
        System.out.println("mul : " + mul);
        System.out.println("div : " + div);
        System.out.println("mod : " + mod);
        
    }
}
