package differentoperators;

public class LogicalOperators {
	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;
		
		System.out.println(a && b);
		System.out.println(a || b);
		System.out.println((!a));
		
		int c = 5;
		int d = 7;
		System.out.println(c > d);
		System.out.println(c < d);
		System.out.println(c == d);
		System.out.println(c >= d);
		
	}

}
