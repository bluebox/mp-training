package Main;
public class Bitwise {
	public static void main(String args[]) {
		int firstNumber=6,secondNumber=4;
		System.out.println("Bitwise AND of "+firstNumber+" and "+secondNumber+" is "+(firstNumber & secondNumber));
		System.out.println("Bitwise OR of "+firstNumber+" and "+secondNumber+" is "+(firstNumber | secondNumber));
		System.out.println("Bitwise XOR of "+firstNumber+" and "+secondNumber+" is "+(firstNumber ^ secondNumber));
	}
}
